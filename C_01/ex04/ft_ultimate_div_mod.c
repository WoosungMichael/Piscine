void	ft_ultimate_div_mod(int *a, int *b)
{
	int	t_div;
	int	t_mod;

	if (*b != 0)
	{
		t_div = *a / *b;
		t_mod = *a % *b;
		*a = t_div;
		*b = t_mod;
	}
}
