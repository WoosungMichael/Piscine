#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = argc - 1;
	while (i > 0)
	{
		j = -1;
		while (argv[i][++j])
			ft_putchar(argv[i][j]);
		i--;
		ft_putchar('\n');
	}
}
